//Create functions add(), sub(), mult(), div() for displaying addition, substraction, multiplication, division of 2 integers.
function add(a, b){
    return  (a + b); 
}

function sub(a, b){
    return  (a - b); 
}
function mult(a, b){
    return  (a * b); 
}
function div(a, b){
    return  (a / b); 
}

console.log(add(2,4));
console.log(sub(30, 15));
console.log(mult(2,4));
console.log(div(30, 5));
